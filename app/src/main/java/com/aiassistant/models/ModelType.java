package com.aiassistant.models;

/**
 * Model Type
 * Types of machine learning models
 */
public enum ModelType {
    /**
     * Q-Learning - Simple reinforcement learning algorithm
     */
    Q_LEARNING,
    
    /**
     * SARSA - Alternative reinforcement learning algorithm
     */
    SARSA,
    
    /**
     * DQN - Deep Q-Network
     */
    DQN,
    
    /**
     * DYNA_Q - Model-based reinforcement learning
     */
    DYNA_Q,
    
    /**
     * CONTEXTUAL_BANDIT - For exploration-exploitation problems
     */
    CONTEXTUAL_BANDIT,
    
    /**
     * DECISION_TREE - For classification problems
     */
    DECISION_TREE,
    
    /**
     * NEURAL_NETWORK - Basic neural network
     */
    NEURAL_NETWORK,
    
    /**
     * CUSTOM - Custom model implementation
     */
    CUSTOM
}